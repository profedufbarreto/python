def soma(a, b):
    resultado = a + b
    return resultado

def subt(a, b):
    resultado = a - b 
    return resultado

def mult(a, b):
    resultado = a * b
    return resultado

def div(a, b):
    resultado = a / b
    return resultado


print(soma(5, 3))
print(subt(8, 2))
print(mult(8, 9))
print(div(18, 9))
    